package modelo;

import java.io.Serializable;

/**
 *
 * @author euclasio
 */
public abstract class Symbol implements Serializable{
    protected String path;

    public Symbol(String path) {
        this.path = path;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
